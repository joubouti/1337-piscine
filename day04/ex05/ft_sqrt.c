int	ft_sqrt(int nb)
{
	int i;

	i = 2;
	if (nb == 0 || nb == 1)
		return (nb);
	while (i * i > 0 && i * i < nb)
	{
		i++;
	}
	if (i * i == nb)
		return (i);
	else
		return (0);
}
