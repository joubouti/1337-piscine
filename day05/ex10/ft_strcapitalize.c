char	*ft_strcapitalize(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (*(str + i))
	{
		if (*(str + i) > 96 && *(str + i) < 123)
		{
			c = *(str + i - 1);
			if (i == 0 || c == ' ' || c == '-' || c == '+')
				*(str + i) -= 32;
		}
		else if (*(str + i) > 64 && *(str + i) < 91)
		{
			c = *(str + i - 1);
			if (i != 0 && c != ' ' && c != '-' && c != '+')
				*(str + i) += 32;
		}
		i++;
	}
	return (str);
}
